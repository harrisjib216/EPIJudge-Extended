function insertLanguage() {
  const languages = [
    ["Kotlin", ".kt", "Java", ".java"],
    ["Rust", ".rs", "C++", ".cc"],

    // todo: later
    // ["TypeScript", ".ts", "Python"]
  ];

  for (const chapter in problem_mapping) {
    for (const problem in problem_mapping[chapter]) {
      for (const [newLang, newFileExt, baseLang, baseFileExt] of languages) {
        let [newKey, newBody] = Object.entries(problem_mapping[chapter][problem]).find((pair) => {
          return pair[0].includes(baseLang);
        });

        newKey = newKey.replace(baseLang, newLang);
        newKey = newKey.replace(baseFileExt, newFileExt);

        problem_mapping[chapter][problem][newKey] = newBody;
      }
    }
  }

  // I run this script in a browser and paste the result
  // into this file. There are better choices but this easiest for me for now
  copy(problem_mapping);
}
